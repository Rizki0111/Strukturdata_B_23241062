# belajar aritmatika dasar
x = 11
y = 4

# penjumlahan +
hasil = x + y
print ("x + y =", hasil)

# pengurangan 
hasil = x - y
print ("x - y =", hasil)

# perkalian 
hasil = x * y
print ("x * y =", hasil)

# pembagian 
hasil = x / y
print ("x / y =", hasil)

# perpangkatan exponen
hasil = x ** y
print ("x ^ y =", hasil)

# modulus %
hasil = y % x
print ("x mod y =", hasil)

# floor division (pembulatan kebawah) //
hasil = x // y
print ("x // y =", hasil)

